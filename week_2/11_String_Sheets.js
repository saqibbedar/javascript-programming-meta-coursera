let person = 'Afsar'

// length property - used for length of char

console.log(person.length); // 5

// charAt property - read each individual character at a specific index in a string 

console.log(person.charAt(0)); // A

// concat() - joins two strings 

console.log(person.concat(" value")); // Afsar value

// indexOf - returns the location of the first position that matches a character: 

console.log(person .indexOf('A')); // A is at 0 index 

// split - Split the sting into a array 

console.log(person.split('s')) // ['Af', 'ar']

// casing of strings

console.log(person.toUpperCase());
console.log(person.toLowerCase());

