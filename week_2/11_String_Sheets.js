let person = 'Saqib'

// length property - used for the length of char

console.log(person.length); // 5

// charAt property - read each character at a specific index in a string 

console.log(person.charAt(0)); // A

// concat() - joins two strings 

console.log(person.concat(" value")); // Afsar value

// indexOf - returns the location of the first position that matches a character: 

console.log(person .indexOf('S')); // S is at 0 index 

// split - Split the string into an array 

console.log(person.split('q')) 

// casing of strings

console.log(person.toUpperCase());
console.log(person.toLowerCase());

