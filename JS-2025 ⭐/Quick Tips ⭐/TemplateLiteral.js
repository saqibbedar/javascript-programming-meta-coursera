// In javascript there are three ways to write an string:
// first one is, using double quotes " ";
// second one is, using single quotes ' ';
// and third way is, using backticks ``;

// which one is better?

// the third one as we can call variables, perform calculations and do many more in a string.

var user = "Saqib Bedar";
console.log(`Hello ${user}, how are you?`);

console.log(`2 + 2 = ${2+2}`) // 2 + 2 = 4
